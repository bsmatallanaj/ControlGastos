    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        // TODO add your handling code here:
        try{
        
        int numDocument = Integer.parseInt(jTextFieldNumDocumento.getText());
        String clave = jTextFieldClave.getText();
        String nombres = jTextFieldNombres.getText();
        String apellidos = jTextFieldApellidos.getText();
        
        String query = "INSERT INTO usuario (usuario_documento,usuario_clave,usuario_nombres,usuario_apellidos) "
                + "VALUES ('"+numDocument+"','"+clave+"','"+nombres+"','"+apellidos+"')";
            
            //validamos el resultado de la consulta
            Statement st = ConexionBd.getConnection().createStatement();
            st.executeUpdate(query);
            if(st.equals(st)){
                JOptionPane.showMessageDialog(this, "USUARIO CREADO EN LA BASE DE DATOS");
                Cuenta cuenta = new Cuenta();
                cuenta.setVisible(true);
                this.dispose();
            }if(espaciosBlanco()){
               JOptionPane.showMessageDialog(this, "INGRESE TODOS LOS DATOS PARA CREAR UN USUARIO");
            }else{
                JOptionPane.showMessageDialog(this, "ERROR EN LA BASE DE DATOS AL CREAR USUARIO");
                limpiarCajas();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

