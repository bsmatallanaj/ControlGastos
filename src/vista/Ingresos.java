    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        try {
            float valor = Float.parseFloat(jTValor.getText());
            String descripcion = jTDescripcion.getText();
            Date fecha = jDateFechaIngreso.getDate();//convertir en strinf y luego de nuevo a fecha
            //String fecha = String.format("%1$td/%1$tm/%1$tY", jDateFechaIngreso.getDate());
            String query = "INSERT INTO ingreso (ingreso_valor, ingreso_descripcion, ingreso_fecha) "
               + "VALUES ('"+valor+"','"+descripcion+"', '"+fecha+"')";
            Statement st = ConexionBd.getConnection().createStatement();
            
            st.execute(query);
            if(st.equals(st)){
                JOptionPane.showMessageDialog(this, "INGRESO CREADO EN LA BASE DE DATOS");
                //Ingresos ingre = new Ingresos();
                //ingre.setVisible(true);
                limpiarCajas();
            }else{
                JOptionPane.showMessageDialog(this, "ERROR  AL CREAR EL INGRESO");
                limpiarCajas();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_jBGuardarActionPerformed

