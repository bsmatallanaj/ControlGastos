    private void jButtonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIngresarActionPerformed
        //recuperamos la informacion de los datos ingresados en los campos
        try {
            String usuario = jTextFieldNumDocumento.getText();
            String clave = String.valueOf(jclave.getPassword());
            String query = "SELECT usuario_nombres AS nombre FROM usuario WHERE usuario_documento = '"+usuario+"' and usuario_clave = '"+clave+"' ";
            Statement st = ConexionBd.getConnection().createStatement();
            ResultSet rs = st.executeQuery(query);
            
            if(rs.next()){
                if(ConexionBd.getConnection() != null){
                    JOptionPane.showMessageDialog(null,"Conexión exitosa" );
                    JOptionPane.showMessageDialog(this, "USUARIO EXISTE EN LA BASE DE DATOS");
                    Cuenta cuenta = new Cuenta();
                    cuenta.setDato(jTextFieldNumDocumento.getText());
                    cuenta.setVisible(true);
                    
                }
        this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "CREDENCIALES INVALIDAD O USUARIO NO EXISTENTE EN LA BASE DE DATOS");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            
        }
            
            
     
        
    }//GEN-LAST:event_jButtonIngresarActionPerformed

